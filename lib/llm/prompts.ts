// 根据 AI 风格选择对应的 SYSTEM_ROLE
export function getSystemRole(aiStyle: AIStyle = 'academic'): string {
  switch (aiStyle) {
    case 'academic':
      return SYSTEM_ROLE_ACADEMIC;
    case 'casual':
      return SYSTEM_ROLE_CASUAL;
    case 'mnemonic':
      return SYSTEM_ROLE_MNEMONIC;
    default:
      return SYSTEM_ROLE_ACADEMIC;
  }
}

